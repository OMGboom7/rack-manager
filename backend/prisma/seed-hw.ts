  for (const c of cpuModels) await p.cpuModel.create({data:c}).catch((e: any) => { if (e.code === 'P2002') dupes++; else console.error('CPU seed err:', e.message); });
  for (const m of memModels) await p.memoryModel.create({data:m}).catch((e: any) => { if (e.code === 'P2002') dupes++; else console.error('Mem seed err:', e.message); });
  for (const s of stoModels) await p.storageModel.create({data:s}).catch((e: any) => { if (e.code === 'P2002') dupes++; else console.error('Sto seed err:', e.message); });
  for (const n of nicModels) await p.nicModel.create({data:n}).catch((e: any) => { if (e.code === 'P2002') dupes++; else console.error('Nic seed err:', e.message); });

  const count = async (t:any)=> p[t].count();
  console.log(`CPU: ${await count('cpuModel')} | Memory: ${await count('memoryModel')} | Storage: ${await count('storageModel')} | NIC: ${await count('nicModel')}${dupes ? ` (${dupes} duplicates skipped)` : ''}`);

  const showBrands = async (t:any) => {
    const r:any[] = await p[t].findMany({select:{brand:true},distinct:['brand'],orderBy:{brand:'asc'}});
    return r.map(x=>x.brand).join(', ');
  };
  console.log('\nCPU brands:', await showBrands('cpuModel'));
  console.log('Memory brands:', await showBrands('memoryModel'));
  console.log('Storage brands:', await showBrands('storageModel'));
  console.log('NIC brands:', await showBrands('nicModel'));
  await p.$disconnect();
